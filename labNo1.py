a=int(input('Введіть першу сторону прямокутника: '))
b=int(input('Введіть другу сторону прямокутника: '))
if a>0 and b>0:
    s=a*b
    p=2*(a+b)
    print(f"Площа прямокутника становить: {s}")
    print(f"Периметр прямокутника становить: {p}")
else:
    print("Одне з чисел від'ємне")
